    public GUI_OrderHistory() {

        initComponents(); 
        Vendor v = Agora.um.vLoggedUser;
        mpanel = new JPanel();
        npanel = new JPanel();
        mpanel.setLayout(new BoxLayout(mpanel, BoxLayout.Y_AXIS));
        npanel.setLayout(new BorderLayout());
        ArrayList<Product> mp = v.viewInventory();
        
        ArrayList<Customer> cs = Agora.um.getCustomers();
        for (Customer c : cs)
        {
            ArrayList<Order> os = c.getOrders();
            for (Order o : os)
            {
                Cart ct = o.getCart();
                ArrayList<Product> ps = ct.getCartItems();
                for (Product pi : ps)
                {
                    for (Product pj : mp)
                    {
                        if(pi.getProductID() == pj.getProductID())
                        {
                            if(pi.getQuantity() > 0)
                            {
                                item = new GUI_OrderLineItem(pi, c);
                                mpanel.add(item);
                                mpanel.setAlignmentX(LEFT_ALIGNMENT);
                                mpanel.setAlignmentY(TOP_ALIGNMENT);
                                npanel.add(mpanel, BorderLayout.NORTH);
                            }
                        }
                    }
                }
            }
        }
        mainPanel.getViewport().add(npanel);
        mainPanel.getViewport().revalidate();
        mainPanel.getViewport().repaint();
    }


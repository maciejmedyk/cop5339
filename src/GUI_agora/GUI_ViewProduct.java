    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Customer c = Agora.um.cLoggedUser;
        Product nP = mP.getClone();
        int qty = (int) mQuantity.getSelectedItem();
        mP.setQuantity(mP.getQuantity()-qty);
        nP.setQuantity(qty);
        ArrayList<Product> pl = c.getCart().getCartItems();
        int count = 0;
        for (Product p : pl)
        {
            if(p.getProductID() == mP.getProductID())
            {
                nP = p;
                nP.setQuantity(p.getQuantity()+ qty);
                count = 1;
            }
        }
        if (count == 0)
        {
            c.addItemToCart(nP);
        }
        Cart ct = c.getCart();
        double amount = ct.getCartAmount();
        mBi.updateCartAmount(Double.toString(amount));
        mBi.repaintMainPanel();
        mQuantity.removeAllItems();
        int q = mP.getQuantity();
        for (int i = 1; i <= q; i++)
        {
            mQuantity.addItem(i);
        }
        DecimalFormat df = new DecimalFormat("####0.00");
        mBi.updateCartAmount(df.format(amount));
        mBi.repaintMainPanel();
    }//GEN-LAST:event_jButton1ActionPerformed


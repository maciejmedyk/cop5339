    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int i = 0;
        for (int k = 0; k < Agora.um.getVendors().size(); k++)
        {
            Vendor user = Agora.um.getVendor(k);
            if (user.getUsername().equals(mUsername.getText()) && user.getEmail().equals(mEmail.getText()) && user.getFullName().equals(mName.getText()) && user.getDateOfBirth().equals(mDate.getText()))
            {
                user.setPassword(mPassword.getText());
                mMessage.setText("Password Change Successfull");
                mMessage.setVisible(true);
                i++;
                try
                {
                    this.serialization();
                }
                catch (Exception e)
                {
            
                }    
                return;
            }
        }
        for (int k = 0; k < Agora.um.getCustomers().size(); k++)
        {
            Customer user = Agora.um.getCustomer(k);
            if (user.getUsername().equals(mUsername.getText()) && user.getEmail().equals(mEmail.getText()) && user.getFullName().equals(mName.getText()) && user.getDateOfBirth().equals(mDate.getText()))
            {
                user.setPassword(mPassword.getText());
                mMessage.setText("Password Change Successfull");
                mMessage.setVisible(true);
                i++;
                try
                {
                    this.serialization();
                }
                catch (Exception e)
                {
            
                }  
                return;
            }   
        }
        if (i == 0)
        {
            mMessage.setText("Password Change Unsuccessfull");
            i = 0;
        }
        mMessage.setVisible(true);
        
        
    }//GEN-LAST:event_jButton2ActionPerformed
    
    private void serialization() throws IOException, ClassNotFoundException
    {
        ObjectOutputStream output = new ObjectOutputStream(new FileOutputStream("agora.dat"));
        output.writeObject(Agora.um);
        output.close();
    }

